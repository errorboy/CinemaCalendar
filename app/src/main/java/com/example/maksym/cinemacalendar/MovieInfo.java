package com.example.maksym.cinemacalendar;

public class MovieInfo {
    private String name;
    private String date;
    private String imgUrl;
    private String url;

    public MovieInfo(String name, String date, String imgUrl, String url) {
        this.name = name;
        this.date = date;
        this.imgUrl = imgUrl;
        this.url = url;
    }
}
